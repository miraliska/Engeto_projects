print("Cvičení: Rozdělení stringu")
# Zadaný string
muj_str = "indexování"
print("Slovo, se kterým se pracuje:", muj_str)

# Prvních 5 znaků
prvnich_pet = muj_str[:5]
print("Prvních pět písmen:",prvnich_pet)

# Posledních 5 znaků
poslednich_pet = muj_str[5::]
print("Posledních pět písmen:",poslednich_pet)

# Každé třetí písmeno od 'i'
kazde_treti = muj_str[::3]
print("Každé třetí písmeno:",kazde_treti)
print("*"*100)
